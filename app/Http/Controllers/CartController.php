    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Product $product)
    {
        $dup = Cart::search(function($cartItem, $rowId) use ($product) {
            return $cartItem->id === $product->id;
        });

        if ($dup->isNotEmpty()) {
            return redirect()->route('cart.index')->with('message', 'Item is already in the shopping cart!');
        }

        Cart::add($product->id, $product->name, 1, $product->price)
            ->associate('App\Models\Product');
            return redirect()->route('cart.index')->with('message', 'Item was added to the shopping cart!');
    }

